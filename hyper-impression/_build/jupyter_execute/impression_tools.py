# # Impression Tools
# 
# The following collection of findings is supposed to enable you to use ***knowledge about the nature of your own mind*** to your advantage, *e.g. in combating habits, regulating emotions and organising your life*.
# 
# ## Introduction
# In every moment of your life, there will be 
# 1. a certain feeling (gut) and
# 2. a certain interpretation (mind)
# about the world around you.
# *Say, you leave the house in an uneasy mood and a person across the street is laughing.\
# Your **feeling** is unease, as I said,\
# but your **interpretation** of the world will be influenced by that:\
# you may belief, that the persn is laughing **at** you.*
# 
# Here is our human problem: We only get to affect our interpretations directly. The feelings, we are at the mercy of. At least aparently ...
# 
# *MEHR ERKLÄRUNG NÖTIG*
# 
# ## 1.Ground Principles of Mind
# 
# ## Interpretation *of* Feelings
# Setze den Glauben ein, dass deine Gefühle nicht die Wahrheit tatsächlich beschreiben.
# Warum reicht das noch nicht? Es ist nötig und hilfreich für verankerte Gefühle von schlechrten Gewohnheiten, aber ein ständiges Battle, das verlangt, dass du immer conscious bleibst kann nciht endziel sein.\
# ALso ist unser Ziel, dass wir uns Gefühle einrichten, die positiv helfen.\
# Ziel: Über impressionen neue interpretationen emotional verankern und reinforcen.
# 
# ## *Deciding* on Interpretations
# MEHR ERKLÄR \
# Wenn jedes Gefühl wiederlegbar ist, dann auch das Gefühl, dass die derzeitige Interpretation wahr ist.
# Somit wird die Interpretation eine völlig freie Entscheidung.
# 
# You can, in certain conscious moments, let go of a held interpretation and *decide* to believe in another one instead. \
# This is especially useful, when you have held beliefs that are in conflict with facts of the real world *e.g. I am convinced this guy hates me, eventhough he does not*, or which are actually *unknowable*, while believing affects you negatively. 
# 
# ## 2.Methods for Impressions
# Wie man gefühle über neu erlernte, besser geeignete Interpretationen der welt verankert, sodass man immer mehr emotional dem Bild entspricht, dass man rational erwünschen würde.
# 
# ## Example catching
# Whenever you get a strong impression about a specific situation, *e.g. "I just realized, that I can do ten pushups without a problem, even though I heavily felt, I don't want to/couldn't before"*, you should not let that opportunity pass.
# #### Sidenote
# We generally overestimate our ability to remember. You might think, "I definitely will remember this pushup-incident, so no need to think much about it", but ponder on this/try this out yourself: \
# **Before going to bed, think of a bunch of things that just crossed your mind. Write them down. Tomorrow morning, read them again and check, if you would have remembered, if you hadn't written them down.**
# #### End Sidenote
# So you realize, that a strong impression also will not stand the test of time, unless it is intently turned into a **powerful impression**, meaning, one, that will come around by itself every now and then, because it has robust foundation in your mind.
# 
# For this, you have to encapsulate the example to stand for the clear impression *(in this case "Pushup-incidence" stores "What I strongly feel to be impossible/undoable still can be.")*. \
# Formulate in words for yourself exactly, what you learned and recognize, that you feel the truth of it in your body, as you have just witnessed this very thing.
# 
# #### Explaination
# We learn from impressionate situations althroughout our childhood. We get a very strong sense of something, as we have witnessed a situation that indicates the truth of this interpretation. And we store this impression emotionally, mostly unquestioned, in our understanding of the world. \
# *As a child I fall into the mud. This induces a vivid notion, that everyone around is looking at me and judging me.\
# I feel with strong emotions, that it is happening now and therefore store the interpretation "Everyone judges me and it feels bad" as a **powerful impression**.*
# 
# The truth of the interpretation is not questioned, just assumed true.
# 
# But now, as we learn to *decide* on interpretations, we can look back and recognize, that this experiences does not mean, the interpretation we gained from it is actually describing the real world. 
# 
# Furthermore, we recognize our inability to ever know accurately what others think and therefore our best option is to choose an interpretation that helps us live our life.
# 
# #### End of Explaination
# 
# But the just described, rather arbitrary process *(just because I fell into the mud, I have paranoia)* can be used intently as well:\
# I want to better my life. Therefore I think about my problems. I recognize some of them. I try to push myself to go beyond them. I experience my ability to overcome them and how I did it.\
# This is the experience we intently turn into a **powerful impression**.
# 
